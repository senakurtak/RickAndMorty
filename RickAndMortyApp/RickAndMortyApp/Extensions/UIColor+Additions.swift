import Foundation
import UIKit

extension UIColor {
    
    @nonobjc class var orgDarkGray: UIColor {
        return UIColor(red: 33/255.0, green: 33/255.0, blue: 33/255.0, alpha: 1.0)
    }
 
    @nonobjc class var pewter: UIColor {
        return UIColor(red: 190/255.0, green: 192/255.0, blue: 193/255.0, alpha: 1.0)
    }
    
    @nonobjc class var pewterLight: UIColor {
        return UIColor(red: 124/255.0, green: 133/255.0, blue: 138/255.0, alpha: 1.0)
    }

    @nonobjc class var darkSlateGray: UIColor {
        return UIColor(red: 47/255.0, green: 79/255.0, blue: 79/255.0, alpha: 1.0)
    }

}
